            <li>玩家的主要敵人是弗拉迪斯拉夫二世，他將經常與玩家和摩爾達維亞戰鬥。雖然奧斯曼帝國和匈牙利都對玩家有敵意，但他們正在忙著互相爭鬥，只會偶爾騷擾玩家。然而，如果玩家進入帝王時代，匈牙利會要求1000食物與玩家結盟。區區1000食物就給他吧，多一個盟友就少一個敵人。地圖中間的特蘭西瓦尼亞村莊有一座城堡可供佔領。</li>
