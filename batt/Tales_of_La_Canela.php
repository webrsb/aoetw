                <li>然後賄賂商人卡洛斯(基多東部的爆炸桶)推遲發貨。當你(法蘭切斯科-德-奧雷亞納)目睹卡洛斯親吻他的情婦並"威脅"告訴他的妻子時，他會命令他的兩名守衛(戟兵)殺死你。你可能需要一些打帶跑戰術來殺死他們，因為戟兵對騎乘部隊是一個很大的威脅(注意法蘭切斯科-德-奧雷亞納是征服者)。然後卡洛斯會同意你的要求。</li>
                <li>無論是在會見皮薩羅之前還是之後，您都可以完成一些“次要目標”收集用於購買庫拉塔部隊的黃金(從基多返回，完成上述主要目標後)，並收集食物招募本地印加單位。一路上你會遇到叛軍，並且必須將他們擊退。</li>
                <li>當你的部隊從基多向東開拔，並朝著皮薩羅在蘇馬科山谷的基地前進時，你將在抵達印加遺址之前遇到兩隊叛軍。至少派遣一名最初的斥侯去皮薩羅的基地會見他。然後，廢墟將暫時成為你的基地，你必須抵禦叛軍，直到你在戰役中共累計殺死100名敵軍。一旦敵人撤退，皮薩羅會再來和你聊天，然後你就會勝利。</li>
