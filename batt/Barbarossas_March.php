         <h2>概述</h2>

        <div class="hrn1"></div>
        <div class="n1">
          <ul>
            <li>在遊戲一開始的時候，你擁有一支由七十個單位組成的軍隊，但其中並沒有村民。你必須沿著道路往南方移動，在那邊你會發現一條小河流。這條河流由薩拉森的戰船駐守，直接摧毀掉就對了，不需要浪費時間，它也沒辦法造成多大的傷害。</li>
            <li>渡過河流後，你會遇到薩拉森的奴隸兵正在摧毀一座房子。由你條頓武士領軍，傷亡應該會降到最低。當一切都結束後，繼續往南前進。記得，別往加里波里去，薩拉森人會在路上到處埋伏，而且你還拿不到你所需要的戰船。往君士坦丁堡的路是比較簡單的，那裡是拜占庭帝國的首都。</li>
            <li>往君士坦丁堡的路上，你將遇到許多哨站，沿著路往前，就會走到君士坦丁堡。君士坦丁堡由雙層城牆、八座火砲塔保護著，一旦靠近，拜占庭帝國就會自動將你改為敵對狀態。（後來的第四次十字軍的不義之舉，在在證明拜占庭皇帝之睿智）總之，要通過城牆，就必須展開進攻：讓你的軍隊、攻城武器遠離君士坦丁堡的火砲塔，以避免傷害。</li>
            <li>當拜占庭的軍隊結束攻擊，運用你的三架巨型投石機摧毀君士坦丁堡的火砲塔、城門。你不需要用你的軍隊去攻擊城門，只要運用巨型投石機就好。一旦城門與火砲塔被毀，就讓你的軍隊前進到君士坦丁堡中的世界奇觀（聖索菲亞大教堂）沿途不需要攻擊任何建築物，尤其是城堡，只要專注移動到奇觀旁邊即可。當你的軍隊到達奇觀旁邊，拜占庭皇帝又會將對你的外交狀態改為盟軍，如此，你便可運用帝國的海軍了。</li>
            <li>但到了這個當下，你又遇到另外一個問題：薩拉森海軍。由於薩拉森人與君士坦丁堡的貿易關係，許多薩拉森船艦靠近拜占庭的海軍，一旦拜占庭的海軍歸你所轄，薩拉森人立馬就會展開攻擊，這時最好先專心打完海戰，然後再登船。薩拉森人離開後，就帶上你的人，往聖地前進吧！一艘拜占庭的運輸船可以裝載20個單位，所以你最多只需要四艘，就能全軍出動。帶上你的戰船以對抗薩拉森的海上攻擊，而且，由於拜占庭的船隻非常快速，你只需要五到十秒就能到達安那托利亞。</li>
            <li>到達安那托利亞後，你只剩下一個敵人：塞爾柱土耳其人，全軍進發吧！你會發現一條通往塞爾柱人城牆的道路，由兩座塔防守，當你到達那裡的時候，會有一場時機恰到好處的地震發生，塔與城堡都會自動倒塌，你可以輕易穿過這個陣地。走南邊會穿過沙漠的道路，你會看到一個小湖泊，通過湖泊後往東，看到條頓人的城牆，那裡就是救傷團的基地。（PS：救傷團就是醫院騎士團）</li>
            <li>到達湖泊的東岸後，你會遭遇一場攻擊，而此時救傷團也會大開城門，塞爾柱人甚至會湧入營地。在這個時間點，你可能無法抵禦這麼強大的攻擊，但救傷團的士兵會適時伸出援手，然後關上城門。最後，你抵達了目的地，任務總算達成。</li>
          </ul>
