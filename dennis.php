              build up a strong fortification composed of Castles, Walls, and Towers,
              so basically perform a late-game turtling strategy. In any case the player
              must be prepared to be the main focus of all enemy attacks and prepare
              to defend the Wonder. It is advised to have a huge army to defend against
              attacking armies, particularly siege units. Since the Wonder takes incredibly
              long to be built, it is advised to build it with as many Villagers as possible
              while also leave them near the area once they finish in case the Wonder
              needs to be repaired. In team games the support of allies while defending
              is highly valuable.</p>
            <p>It is possible (but costly) to create more than one Wonder so that if
              one does not stand, other Wonders can stand for the time required to win
              the game.</p>
