          <h3>歷史</h3>
          <div class="hrn1"></div>
          <div class="n1">
            <p>輕騎兵是騎馬的輕裝部隊，與穿著沉重鎧甲（有時包括馬）重騎兵相對，輕騎兵的主要任務基本上是偵查、掩護、散兵、突襲，並且他們裝備矛、劍、弓和之後出現的手槍。</p>
            <p>古希臘和古羅馬，較少運用輕騎兵，儘管羅馬輔助部隊經常騎馬，但是中亞遊牧民族卻經常使用輕騎兵。而匈奴、突厥、蒙古人和馬扎爾人都很擅長輕騎兵和弓騎兵。</p>
            <p>隨著歐洲封建制度及騎士的沒落，在歐洲大陸中，輕騎兵成為重要的作戰部隊。許多輕騎兵都裝備了火器以替代老式的弓箭。 歐洲人使用輕騎兵的例子還包括斯抓迪亞特、哈伯勒、驃騎兵、、哥薩克、及一些龍騎兵。</p>
