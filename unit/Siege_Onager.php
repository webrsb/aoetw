            <h3>歷史</h3>
            <div class="hrn1"></div>
            <div class="n1">
              <p>投石機或稱拋石機、「 回回炮」,砲或炮，是遠古時代與中古時代出現的攻城武器，可把石彈、巨石或爆裂物投到敵方的城牆、城內或者兵陣內而造成破壞。</p>
              <p>漢語的「砲」便是古漢語動詞「拋」的名詞形式，這種通過聲調改變詞性的手段被稱為四聲別義。「砲」原指投石機與其彈，唐後也指由投石機發射的燃燒物，宋後也指由投石機發射的縱火物與爆裂物，明後也指大型管身火器。宋代之前「砲」與「炮」不同義，「炮」只有「燒烤」的意思，宋代之後「炮」逐漸有「砲」的意思。就連「火砲」一詞最初也是指發射燃燒物或爆裂物投石機與其砲彈，明後才也指大型管身火器。</p>
