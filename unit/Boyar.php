          <div class="hrn1"></div>
          <div class="n1">
            <ul>
              <li>貴族鐵騎擁有遊戲中騎兵類最強大的近防裝甲，不過遠防和血量相對的比遊俠(聖騎士)還來的低，使遠程作戰單位容易對它們造成更多傷害。</li>
              <li>貴族鐵騎在遊戲設定上是為了彌補沒有遊俠的斯拉夫但跟遊俠有所區別。</li>
