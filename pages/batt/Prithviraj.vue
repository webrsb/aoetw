          當中包括：《布里斯比羅吉讚歌》(個人翻譯，作者 佚名 Anonymous)、
          《哈米爾偉頌歌》(個人翻譯，作者 納亞強達。舒利 Nayachandra
          Suri）及《地王頌》。
          <p>
