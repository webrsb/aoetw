            在作品集《阿喇亢德》(Alha-Khand)中，昌德拉詩人犍嘎尼卡(Jaganika)也提供了一個誇大昌德拉人與布里斯比羅吉之間戰爭的作品與後世參考。
          </p>

          <p>教育程度</p>
          <p>中古時期的布里斯比羅吉傳記裡，指出他的教育程度良好。</p>
          <p>
