          <h3>歷史</h3>
          <div class="hrn1"></div>
          <ul>
            <li>
              該戰役不是由Ensemble Studios編寫的，而是由設計者安德烈亞斯·馬歇德（更名為aMa）編寫的戰役，
              他根據戰役的自述文件於2000年5月28日完成了戰役。
            </li>
            <li>該戰役是2000年微軟舉行的查里曼戰役競賽的獲勝者，也是將其添加到《世紀帝國II：征服者入侵》擴充版的原因。
            儘管它已包含在遊戲文件中，但在戰役菜單中看不到也無法遊玩，以防止侵犯版權。 因此遊玩該戰役的唯一方法是將戰役
            文件從光碟的“ Goodies”文件夾移動到遊戲的戰役文件夾。</li>
            <li>
              即使該戰役最初只有德語內容和旁白，相應國家/地區也可以從首次發售的擴充版遊戲光碟中獲得其他語言的版本。
              除了不同的語言版本外，還存在“帝王時代”（.scn），“征服者”（.scx）和“帝國時代II” HD（.aoe2scenario）場景的單獨版本。
              安德烈亞斯·馬歇德（Andreas Marscheider）本人建議使用《世紀帝國II:帝王世紀》的原始版本，因為那是根據該版本的遊戲所設計的戰役。
            </li>            
            
          </ul>
