<h3>攻略</h3>
<div class="hrn1"></div>
<ul>
  <li>遊戲一開始有十幾個軍事單位與必須保護的貿易車，帶領他們以最快速度進入木曾義仲的城寨，若碰到敵軍則盡量以騎兵引誘，其他單位砍殺的方式解決，能夠減少傷亡。</li>
  <li>木曾義仲的城寨裡面有不少軍隊，而礦藏都在城外，把軍隊部署在外面以保護你的村民。</li>
  <li>城寨東邊有一座農場，但是沒有城門，補上城門好安然採集食物。</li>
  <li>先挖取石頭來蓋城堡，城堡蓋在離礦藏近的地方，或是直接蓋在第一個平家軍軍營的門口，利用城堡來壓制他們。</li>
  <li>敵人大部分以步兵為主，馬弓騎兵能夠有效防衛，但是別忘了針對衝撞車生產一些步兵。</li>
  <li>敵人不會生產投石機，所以城堡加上數個箭塔，再搭配十幾個長槍兵就足以防守。</li>
  <li>木曾義仲基地南方有個金礦地，清除第一個前線基地之後可以在這裡建築城堡防守。</li>
  <li>北条氏可以協助你清理地圖上的平家守衛，兩台投石機是很好的投資。</li>
  <li>必須把四個平家軍的前線基地拆除(所有軍事建築)，還有五位平家將軍殺死，才能觸發源氏劇情。</li>
  <li>五位敵方將軍分別是：靠最近的平通盛，南邊的平經正，中央基地城堡前的平知盛，靠近西方基地的平忠度，以及北方與平家守衛一同佔領燈塔的平清房。</li>
  <li>海戰不是必要的，若要使用海戰來殲滅平家軍的前線基地，則必須先殺死平清房奪取燈塔。</li>
  <li>源氏帶來的軍隊是很強大的騎兵，擁有8/6的防禦，12的攻擊力，還有250的變態生命值與自然回血。只要他們沒有死，都可以加入下一場戰鬥。</li>
  <li>京都的防守非常厚實，善用投石機。</li>
  <li>海軍無法傷到京都，注意投資的量。</li>
</ul>
<h3>遊戲內細節</h3>
<div class="hrn1"></div>
<ul>
  <li>遊戲中的"燈塔"看起來就是韓國的世界奇觀。</li>
  <li>除了京都之外的敵方勢力都不能進入帝王時代。</li>
  <li>電腦很笨，不會出投石車。</li>
  <li>所有宮殿看起來都一樣，但是真正的金閣寺只有在奈良。</li>
  <li>很神奇的，源氏帶來的軍隊沒有中文名稱，英文名稱是Elite Mounted Samurai，這是很奇怪的名稱，因為遊戲內沒有Mounted Samurai這種單位，而Elite是精銳的意思。</li>
  <li>木曾義仲把城寨與兵權交給玩家之後似乎就去睡覺了(從地圖上消失)。</li>
  <li>你可以吸引平清盛遠離京都裡面的紅色世界奇觀，並用你的部隊靠近，屆時世界奇觀會變成你的藍色。</li>
  <li>北条通常從他們基地的南方出兵，而那裏是最沒有效率的。</li>
  <li>地圖上的狼全都是恐狼。</li>
</ul>
<h3>歷史</h3>
<div class="hrn1"></div>
<ul>
  <li>這場戰役描寫的其實不只是俱利伽羅峠之戰一役而已，而是從那次戰役一直到攻陷京都。</li>
  <li>俱利伽羅峠指的是越中國與加賀國的邊境關口，是個險要的斷崖。</li>
  <li>源氏其實就是源賴朝，真正的源家掌權者。</li>
  <li>木曾義仲的本名是源義仲，是源賴朝的表親，因為在木曾谷長大而得此名。</li>
  <li>歷史上的北条時政是源賴朝的岳父，但其實跟木曾義仲不合，在真正的俱利伽羅峠之戰也不曾出手幫助過木曾義仲。後來甚至親手消滅了義仲的勢力。</li>
  <li>本戰役的最終大魔王平清盛，歷史上其實早在這場戰役發生的兩年前死於熱病。而歷史上真正領導這場戰役的平維盛則連露臉都沒有。</li>
  <li>歷史上真正守衛能登國(遊戲中的"燈塔")的是平通盛與平忠度，而不是平清房。</li>
  <li>據說木曾義仲在此戰中曾使用中國田單的火牛戰術，後來認為事後是捏造。</li>
  <li>京都在古代日本被分類為畿內，其實在歷史上沒有這麼堅固，反而因為地處平原周圍環山而常常被攻陷。</li>
  <li>遊戲地圖就是整個日本中部，但是形狀跟比例似乎怪怪的。</li>
  <li>本次大魔王平清盛其實根本沒當過王，他至多只能算個國防部長。</li>
  <li>源賴朝本人沒有參與俱利伽羅峠之戰。</li>
  <li>小魔王之一的平知盛其實活到了源平合戰的最後一戰。</li>
  <li>歷史上的木曾義仲確實曾經進軍並掌管京都，但是卻因為剛愎自用與縱容軍隊暴行，最終被源家討伐。</li>
  <li>真正的平氏在1185年的壇之浦之戰滅亡。</li>
  <li>源家的勢力在1333年與鎌倉幕府一起進入了歷史，前北条家也滅亡(後北条就是戰國有名的北条早雲，世紀帝國II日本AI的常用名稱)。</li>
  <li>源平合戰最終導致日本真正進入數百年的幕府時代，也可以算是封建時代的開端。</li>
</ul>
