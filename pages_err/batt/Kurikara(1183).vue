<h3>斥侯</h3>
<div class="hrn1"></div>
<div class="n1">
<ul>
  <li>源賴朝已經往礪波山(紫色)派遣了一支援軍(藍色)，以協防這片兵家必爭之地，礪波山城高牆聳立，是源賴朝的領地，目前由他的堂弟木曾義仲駐守。</li>
  <li>北条氏(黃色)在東邊有一座城池，由他們的首領北条時政所掌管。北条時政痛恨平家，只希望在破解礪波山之圍之後加入源氏勢力。</li>
  <li>平家軍(橘色)是你早期的主要對手。想要給礪波山解圍的話，你必須把他們前置的攻城武器場摧毀，注意還有三座軍營在守衛河道。</li>
  <li>平氏守衛(綠色)在城外四處巡邏，他們的村民為平家提供了糧草供應。</li>
  <li>平氏的主城京都(青綠色)固若金湯，由大量防衛塔和城堡守衛著，從未被攻破過，倘若城牆被攻破，大批的敵軍就會衝去防守。</li>
  <li>平家軍的首領是一幫兇殘的武將(紅色)，尤其是他們的主公平清盛本人。擒賊擒王，想要取勝，你得想方設法幹掉敵人那些帶頭的武將。</li>
</ul>
