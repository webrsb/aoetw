            <p>勒班陀戰役（英語：Battle of Lepanto，1571年10月7日），或譯勒潘陀海戰，是以西班牙帝國、威尼斯共和國為主力的神聖同盟（Holy
              League）艦隊與鄂圖曼帝國海軍在希臘愛奧尼亞海帕特拉斯灣展開的一場海戰。神聖同盟艦隊在海戰中取得決定性勝利。鄂圖曼帝國海軍自勒班陀的海軍港口出發向西，與自西西里墨西拿出發向東航行的神聖同盟艦隊遭遇。
              神聖同盟（Holy League）是由教宗庇護五世主導成立的聯盟，包括許多歐洲天主教海洋國家。在西班牙國王腓力二世和威尼斯共和國的一致同意下，神聖同盟艦隊由西班牙國王腓力二世同父異母的弟弟，奧地利的唐胡安統領。</p>
            <p>勒班陀戰役是西方自古典時代以來最大的海戰。在海戰史上，勒班陀戰役標誌著歷史上最後一場以槳帆船為主的大型海戰。這場戰役在本質上是一場「甲板上的步兵戰鬥」。在之後的幾十年中，蓋倫帆船和戰列線戰術在海戰中的重要性持續上升，取代了槳帆船在海戰中的主力戰船地位，開啟了「航海時代」。</p>
            <p>為了抵抗鄂圖曼帝國在16世紀的擴張，哈布斯堡王朝與其基督教同盟聯手抵禦鄂圖曼大軍。而勒班陀海戰就是發生於1571年的地中海，是一系列衝突的高峰。
              由唐璜率領的天主教海軍最終將鄂圖曼艦隊給擊潰，扭轉了兩方原有的情勢。而此戰役也因為廣泛的運用火器而影響了之後的海戰風貌。至此，西班牙無敵艦隊誕生，不過鄂圖曼很快的重組艦隊，讓天主教聯盟也不敢貿然於地中海再啟戰端。</p>
