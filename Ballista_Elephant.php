          <div class="hrn1"></div>
          <div class="n1">
            <ul>
              <li>弩砲象跟<a href="Portuguese.php">葡萄牙</a>的<a href="Organ_Gun.php">手風琴砲</a>，同樣是特殊單位屬攻城武器單位。跟大部分攻城武器的優勢和劣勢一樣擁有範圍射程對大量單位效果好，但注意的是他可以用
