            <h2>芝麻小事</h2>
            <div class="hrn1"></div>
            <div class="n1">
              <ul>
                <li>萬里長城的箭塔會在遊戲的第一秒自動升級為大型箭塔，這與關卡難度無關。</li>
                <li>擁有征服者入侵的資料片(或者HD版本)，會讓事情變得很容易：在城堡時代你就能生產一堆破壞者，然後把他們送到金國的領土上。</li>
                <li>然後，你可以用其中一些搞定城牆、再用其他的摧毀奇觀。如此一來，防止金國的勝利，讓你得以專注在其他中國敵人上、也讓你的軍力更加壯盛，最後再來對付金國。這個奇觀被摧毀後，金國在剩下的遊戲時間裡不會再蓋一個。</li>
                <li>金國會動用超過30個村民建造奇觀，所以建成速度不僅會比匈奴戰役第六關其他敵人蓋的奇觀來得快、也會比阿茲特克戰役中更快，是劇情之中最快的。</li>
                <li>雖然你只需要擊敗金、宋、西夏與突厥，但只要工程師仍然存在，關卡就不會結束。</li>
                <li>這關的宋，指的是南宋。在當時是軍事上相對「弱勢」的王朝、被鄰國持續圍攻。不過，直到成吉思汗過世50年後，他的孫子忽必烈才攻滅南宋、統治中國。（金國也是成吉思汗死後才被蒙古人攻滅）</li>
                <li>金，在中文中意指黃金，也許這是遊戲設計者使用黃色代表金國的原因。金是由女真人所建立的王朝，在當時是宋朝最大的外患，甚至超過了蒙古人，他們的後裔又以滿洲人為人所知，後來征服了中國（明朝）也就是四百年後的清朝。</li>
                <li>西夏人是一個由党項人建立的較小王國，位在當時中國的西北方，在軍事上也對宋朝形成威脅。党項人就是當時活躍於中國西北方的遊牧民族。（這裡所說的中國指的是南宋，不過值得注意的是此時的金國也佔據所謂的中原地帶，所以真正代表中國的政權仍有待商榷）</li>
                <li>党項人與西夏應是同樣的政治實體，而不是分開的。事實上成吉思汗聯合宋攻打金國。</li>
