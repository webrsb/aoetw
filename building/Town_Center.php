          <h3>戰術</h3>
          <div class="hrn1"></div>
          <div class="n1">
            <ul>
              <li>城鎮中心是遊戲中最重要的建築物之一，它是每個玩家的核心，也是生產每個玩家發展帝國骨幹所需村民的唯一途徑。</li>
              <li>一旦城堡時代到來，建立更多的城鎮中心是至關重要的，因為它允許玩家增加村民的數量和撐起整個經濟。城鎮中心應盡量建造在資源旁邊(有兩種以上資源就可考慮蓋城鎮中心)。在城堡和帝國時代失去城鎮中心往往會阻礙玩家的經濟，所以他們往往是攻擊的目標。</li>
