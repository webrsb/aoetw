            <p>在隨機地圖中每個玩家在開始時都有一座城鎮中心。只有演進到城堡時代才能建造額外的城鎮中心，若在城堡時代前城鎮中心就被破壞那遊戲幾乎就結束了。在特定地圖中(例如游牧地圖)城堡時代前玩家可自由選擇蓋城鎮中心的位置，若不喜歡可毀掉之後在新地點重蓋一個新的。</p>
            <p>城鎮中心作為庇護所可駐紮高達15個單位，村民在城鎮中心周圍工作十分安全，敵方來襲時只要敲響警戒鐘讓村民躲入城鎮中心放箭，直到敵人被消滅或退去後再次敲鐘返回工作崗位即可。弓兵也可進入城鎮中心增加城鎮中心的攻擊力與放箭數。</p>
            <p>小技巧:若要讓村民回到城鎮中心時可選擇不要敲警戒鐘，而是圈選後手動駐紮在城鎮中心，這樣可以讓靠近城鎮中心的村民達到瞬間移動的效果。</p>
