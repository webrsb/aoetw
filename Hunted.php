<h2>攻略</h2>
<div class="hrn1"></div>
<div class="n1">
  <ul>
<li>1.  一開始你只能往西北方走，沿路上會遇到一些軍隊加入你，
也會遇到一些敵人。由於困難模式一開始守城會很累，所以盡量保持兵力比較好。
除了最一開始的2投石車3步兵之外（用你的英雄上去單挑，沒血就先跑），
其餘的小規模敵軍都可以小心繞過。可以將你的部隊設為不還擊狀態，
每繞過一批敵軍就存一次檔。一共會拿到3騎士3長槍兵4馬弓4弩兵。</li>

<li>2.  過河的時候會遇到一批大的敵軍，善用兵種相剋跟控兵技巧把損傷降到最低吧！</li>

<li>3.  過河之後，先不要去接觸村莊，因為一接觸村莊敵人就會開始生兵進攻，
先用你的英雄把周圍環境探勘完畢。村莊西南方有一個小型的敵軍基地，
用你的英雄去把木牆拆了，先殺投石車再殺弓兵，裡面有一坨金礦待會可以考慮開採。</li>

<li>4.  這裡有一個偷吃步，由於你的英雄會回血，還有3點遠防，
所以箭塔在短時間內是無法將他殺死的。由於取得城鎮之前敵人不會生兵，
也只有極少量的守軍，所以可以讓你的英雄掩護其他部隊直接衝入敵營，
毀掉一定數量的軍事設施即可獲得勝利。</li>

<li>5.  如果不想要偷吃步的話。探勘完周圍地形，就去接收黃色城鎮（會獲得500木、700食、450金、50石）。</li>

<li>6.  黃色城鎮木牆蓋成那副德性，位置也不好，實在是難攻難守，並不是一個紮營的好地方。
因此先用所有食物生產村民，旗幟插在村莊西方的一處空地，
那裏有好幾隻斑馬、兩頭大象、更西邊還有一大坨金礦。不斷生村民來殺動物。盡速衝城堡。</li>

<li>7.  村民生到一半敵軍就來了，此時小心不要讓城鎮中心剛生的村民把敵軍引過來。
只要看到敵軍，立刻就把旗幟插在城鎮中心自己身上，
這樣可以用剛生好的村民射箭弄死那些小兵，等小兵一死，
立刻將村民帶出來我們的臨時營地。如果不小心有一兩隻敵軍發現我們的營地，
立刻用你的英雄跟剩下的騎士去弄死他們。</li>

<li>8.  點下升級城堡之後，派人去把村莊北方的石礦區占滿，
剩下的留兩人種田，其他去村莊北方伐木。
敵人會專心對付你的城鎮中心，暫時不會去騷擾你北方的村民。</li>

<li>9.  升城堡之後，立刻集結所有村民在你的臨時營地蓋一座城鎮中心跟城堡。
兩者距離一個農田寬，互相照應。之後就努力爆經濟、點科技。
優先把城垛射孔跟彈道學點出來，再出一堆弩兵把城堡塞滿，
你的村民就安全了。注意敵人在你升城堡之後會出衝車來撞城。
所以要盡速生出幾隻飛刀女兵來對付衝車。
（但要小心等衝車靠近再攻擊，否則飛刀很脆弱，被敵人弓箭手弄幾下就掛了）</li>

<li>10. 接下來一直出騎士，掩護你的村民到處採礦，再出一支僧侶放在城鎮旁邊幫忙補血，
等騎士累積到20隻，直接衝到敵人家摧毀軍事建築取得勝利。不小心被全軍覆沒就再出第二波直到勝利為止～</li>

<li>11. 出兵攻城時，要小心敵人的衝車會憑空生出來，
並且直衝你家，不會理會你的進攻部隊，
所以如果沒有一口氣結束遊戲的把握，就要小心家裡城堡有沒有衝車在攻城～</li>
</ul>

