           <h3>歷史</h3>

          <div class="hrn1"></div>
          <div class="n1">箭狹槽發明於公元前214-212圍攻錫拉丘茲期間，發明人是阿基米德。"這個狹縫是一個手掌寬的寬度和一個人身高的高度"防守者藉由這個細縫由內而外射出箭矢，或是弩砲。儘管在希臘羅馬時代就有記載，但在城堡時期並沒有出現這個防禦措施。直到12世紀末再次被人們想起並且使用在軍事建築中。英格蘭的
            <a
            href="http://tinyurl.com/y9sletxz">多佛爾城堡</a>與<a href="https://en.wikipedia.org/wiki/Framlingham_Castle">弗拉姆林陵堡</a>、
              <a
              href="Richard_the_Lionheart.php">獅心王查理</a>在法國的<a href="https://en.wikipedia.org/wiki/Château_Gaillard">Château Gaillard</a>都有使用。箭狹槽被定位於保護城牆的一部分，而不是整座城堡。到了13世紀在城堡使用箭狹槽的防禦工事已經很常見了。</div>
